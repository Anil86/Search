        private int FindElement(int[] array1, int[] array2, int n, int m, int k)
        {
            if (n == 0) return array2[k];
            if (m == 0) return array1[k];


            int mid1 = n / 2,
                mid2 = m / 2;

            if (k <= mid1 + mid2)
                return array1[mid1] < array2[mid2]
                    ? FindElement(array1, array2, n, mid2, k)
                    : FindElement(array1, array2, mid1, m, k);

            return array1[mid1] < array2[mid2]
                ? FindElement(array1.AsSpan(mid1 + 1).ToArray(), array2, n - mid1 - 1, m, k - mid1 - 1)
                : FindElement(array1, array2.AsSpan(mid2 + 1).ToArray(), n, m - mid2 - 1, k - mid2 - 1);
        }



